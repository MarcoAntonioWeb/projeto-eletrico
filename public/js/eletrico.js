// Vai abrir a janela para imprimir
function gerarPdf() {
    
    const conteudo = document.querySelector(".todo").innerHTML

    // Estilização como vai abriar a janela para impressão
    let estilo = "<style>"
    estilo += "card {windth: 100%; }"
    estilo += "div {font: 39px Arial;}"
    estilo += "h4 {font: 43px Arial; text-aling: center;}"
    estilo += "card-imp {padding: 4px 8px;}"
    estilo += "span {display: none;}"
    estilo += "button {display: none;}"
    estilo += "</style>"

    // Comando para abrir a janela de impressão
    const win = window.open('','','height=700,width=750')

    win.document.write('<html><head>')
    win.document.write('<title>Relação de Material</title>')
    win.document.write(estilo)
    win.document.write('</head><body>')
    win.document.write(conteudo)
    win.document.write('</body></html>')

    win.print()
}

